Query all columns (attributes) for every row in the CITY table.

The CITY table is described as follows: 
+-------------+-------------+
| Field       | Type        |
+-------------+-------------+
| ID          | NUMBER      |
| NAME        | VARCHAR(17) |
| COUNTRYCODE | VARCHAR(3)  |
| DISTRICT    | VARCHAR(20) |
| POPULATION  | NUMBER      |
+-------------+-------------+

SOLUTION:

SELECT * FROM CITY;
